package com.codeforces.round633div2

/** A. Filling Diamonds
  * - https://codeforces.com/contest/1339/problem/A
  *
  * Wherever you put vertical diamond at some point, all other places are uniquely placed by horizontal diamonds.
  */
object A extends App {
  val t = scala.io.StdIn.readInt()

  (0 until t).foreach { _ =>
    val n = scala.io.StdIn.readInt()

    println(n)
  }
}
